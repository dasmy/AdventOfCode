from collections import defaultdict, deque

positions = set()
for row, line in enumerate(input.split('\n')):
    for col, char in enumerate(line):
        if char == '#':
            assert (row, col) not in positions
            positions.add((row, col))
        else:
            assert char == '.'


def neighbours(row, col, dir):
    if dir == 'c_all':
        return (row, col)

    n = set()
    if dir in ('c_north', 'north', 'all'):
        n.add((row - 1, col))
    if dir in ('c_east', 'east', 'all'):
        n.add((row, col + 1))
    if dir in ('c_south', 'south', 'all'):
        n.add((row + 1, col))
    if dir in ('c_west', 'west', 'all'):
        n.add((row, col - 1))

    if dir in ('north', 'all'):
        n.add((row - 1, col - 1))
        n.add((row - 1, col + 1))
    if dir in ('east', 'all'):
        n.add((row - 1, col + 1))
        n.add((row + 1, col + 1))
    if dir in ('south', 'all'):
        n.add((row + 1, col - 1))
        n.add((row + 1, col + 1))
    if dir in ('west', 'all'):
        n.add((row - 1, col - 1))
        n.add((row + 1, col - 1))

    if dir in ('c_north', 'c_east', 'c_west', 'c_south'):
        assert len(n) == 1
        return n.pop()
    elif dir in ('north', 'east', 'west', 'south'):
        assert len(n) == 3
    elif dir == 'all':
        assert len(n) == 8
    else:
        assert False

    return n


rules = deque(['north', 'south', 'west', 'east'])


for round in range(10):
    next_positions = defaultdict(list)
    for row, col in positions:
        rule = 'all'
        if len(positions.intersection(neighbours(row, col, rule))) == 0:
            next_positions[neighbours(row, col, 'c_' + rule)].append((row, col))
        else:
            for rule in rules:
                if len(positions.intersection(neighbours(row, col, rule))) == 0:
                    next_positions[neighbours(row, col, 'c_' + rule)].append((row, col))
                    break
            else:
                next_positions[(row, col)].append((row, col))

    new_positions = set()
    for new, old in next_positions.items():
        if len(old) == 1:
            new_positions.add(new)
        else:
            new_positions.update(old)

    assert len(new_positions) == len(positions)
    if new_positions == positions:
        assert False  # we want to finish 10 rounds
        break
    else:
        positions = new_positions
        rules.rotate(-1)

    print(round, positions)

min_row = min(p[0] for p in positions)
max_row = max(p[0] for p in positions)
min_col = min(p[1] for p in positions)
max_col = max(p[1] for p in positions)

print((max_row - min_row + 1) * (max_col - min_col + 1) - len(positions))
