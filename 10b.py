from collections import deque

POINTS = {
    ')': 1,
    ']': 2,
    '}': 3,
    '>': 4,
}


def parse_brackets(input):
    all_scores = []

    for line in input.split('\n'):
        line = line.strip()
        if len(line) == 0:
            continue

        stack = deque()
        for char in line:
            if char == '(':
                stack.append(')')
            elif char == '<':
                stack.append('>')
            elif char == '[':
                stack.append(']')
            elif char == '{':
                stack.append('}')
            else:
                expected = stack.pop()
                if char != expected:
                    assert char in POINTS.keys()
                    break
        else:
            # This is an incomplete line
            completion = ''
            total = 0
            while len(stack) > 0:
                char = stack.pop()
                completion += char
                total *= 5
                total += POINTS[char]
            print(completion, total)
            all_scores.append(total)

    s = all_scores[:]
    s.sort()
    print(f'Total points: {all_scores}, Middle: {s[(len(s))//2]}')


